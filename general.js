// פונקציה שתדאג להציג את רשימת השירים
function renderSongList() {
    const songsListDiv = document.getElementById('songsContainer');
    songsListDiv.innerHTML = ''; // ננקה את התוכן הקודם
    songsListDiv.className = "song";

    songs.forEach((song, index) => {
        const songTitle = document.createElement('h2');
        songTitle.innerText = song.title + " ▼"
        songTitle.dir = 'rtl'


        // הוספת איוונט שמבצע שינוי ב-showDetails
        songTitle.addEventListener('click', () => {
            song.showDetails = !song.showDetails;
            renderSongList(); // עדכון הרשימה כדי להראות את התוכן של השיר אם צריך
        });

        songsListDiv.appendChild(songTitle);

        // אם showDetails הוא true, נציג את התוכן
        if (song.showDetails) {
            songTitle.innerText=songTitle.innerHTML.substring(0, songTitle.innerHTML.length - 2);
            songTitle.innerHTML+="▲"
            const audio = document.createElement('audio');
            audio.controls = true;
            const source = document.createElement('source');
            source.src = song.audioUrl;
            source.type = 'audio/mp3';
            audio.appendChild(source);

            const lyrics = document.createElement('p');
            lyrics.innerText = song.lyrics;
            lyrics.id = id = "modalText"
            songsListDiv.appendChild(audio);
            songsListDiv.appendChild(lyrics);
        }
    });
}

// קריאה לפונקציה כדי להציג את הרשימה
renderSongList();